package org.liquidplayer.examples.bittorrentservice;

import android.support.annotation.NonNull;
import android.support.v7.app.AppCompatActivity;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;

import java.util.List;

class CustomArrayAdapter extends ArrayAdapter<ExpandableListItem> {

    private final ExpandingListView mListView;

    CustomArrayAdapter(AppCompatActivity context, List<ExpandableListItem> data,
                        ExpandingListView listView) {
        super(context, 0, data);
        mListView = listView;
    }

    @Override
    public @NonNull View getView(final int position, View convertView, @NonNull ViewGroup parent) {
        ItemView itemView = (ItemView) convertView;
        if (null == itemView) {
            itemView = ItemView.inflate(parent);
            itemView.setItem(getItem(position));
            itemView.setListView(mListView);
        }
        return itemView;
    }
}